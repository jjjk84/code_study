-- 대여 시작일을 기준으로 2022년 8월부터 2022년 10월까지 총 대여 횟수가 5회 이상인 자동차들
-- 해당 기간 동안의 월별 자동차 ID 별 총 대여 횟수
-- 월을 기준으로 오름차순 정렬하고, 월이 같다면 자동차 ID를 기준으로 내림차순 정렬
-- 특정 월의 총 대여 횟수가 0인 경우에는 결과에서 제외

SELECT MONTH(start_date) AS MONTH
     , CAR_ID
     , COUNT(HISTORY_ID) AS RECORDS
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE CAR_ID IN (
    SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE START_DATE BETWEEN '2022-08-01 00:00:00' AND '2022-10-31 23:59:59'
    GROUP BY CAR_ID
        HAVING COUNT(HISTORY_ID) >= 5
) 
    AND START_DATE BETWEEN '2022-08-01 00:00:00' AND '2022-10-31 23:59:59' 
GROUP BY MONTH, CAR_ID
ORDER BY MONTH, CAR_ID DESC;

